const generateDateBefore = (daysBefore, type) => {
  let date = new Date();
  date.setDate(date.getDate() - daysBefore);
  let time = `${date.getDate().toString().padStart(2, "0")}.${(
    date.getMonth() + 1
  )
    .toString()
    .padStart(2, "0")}.${date.getFullYear()}`;
  if (type && type === "comment") {
    return date;
  }
  return time;
};

